const Sequelize = require('sequelize');

const DbHandler = require('../../helpers/dbHandler')

const UserDao = DbHandler.getDbInstance().define('user', {
  id: {
    type: Sequelize.INTEGER,
    primaryKey: true,
    autoIncrement: true,
  },
  name: {
    type: Sequelize.STRING,
    allowNull: false,
  },
  email: {
    type: Sequelize.STRING,
    allowNull: false,
  },
  password: {
    type: Sequelize.STRING,
    allowNull: false,
  }
})

module.exports = UserDao;